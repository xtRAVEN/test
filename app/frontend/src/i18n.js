export const intl = createIntl(
  {
    locale: currentLocale,
    messages: messages[currentLocale],
  },
  cache
)

export const changeLanguage = (locale) => {
  currentLocale = locale
  localStorage.setItem('language', locale)
  Object.assign(intl, createIntl({
    locale: currentLocale,
    messages: messages[currentLocale],
  }, cache))
}